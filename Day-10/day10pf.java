// Day 10: Programming Fundamentals: LeetCode 319. Bulb Switcher
public class day10pf {
    public int bulbSwitch(int n) {
        return (int)Math.sqrt(n);
    }
    public static void main(String[] args) {
        day10pf obj = new day10pf();
        System.out.println(obj.bulbSwitch(9));
    }
}
